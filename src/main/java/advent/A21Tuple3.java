package advent;

public class A21Tuple3 {
    private int cardinality;     //számosság
    private int steps;

    public A21Tuple3(int cardinality, int steps) {
        this.cardinality = cardinality;
        this.steps = steps;
    }

    public int getCardinality() {
        return cardinality;
    }

    public int getSteps() {
        return steps;
    }

}
